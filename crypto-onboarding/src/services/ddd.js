/**
 * Identifica o estado pelo DDD
 * @param {string} phone - Telefone no formato (XX) XXXXX-XXXX ou apenas os dígitos
 * @returns {Object|null} Objeto com estado, nome e bandeira ou null se não encontrado
 */
export function getEstadoByDDD(phone) {
  // Remove tudo que não é número
  const digits = phone.replace(/\D/g, '')
  
  // Pega os 2 primeiros dígitos (DDD)
  if (digits.length < 2) return null
  
  const ddd = parseInt(digits.substring(0, 2))
  
  return DDD_MAP[ddd] || null
}
